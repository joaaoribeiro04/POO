using System;

/// <summary>
/// Representa um tipo de comida que pode ser oferecido aos animais do zoológico.
/// </summary>
class TipoComida
{
    /// <summary>
    /// Obtém ou define o nome do tipo de comida.
    /// </summary>
    public string? Nome { get; set; }

    /// <summary>
    /// Obtém ou define a descrição do tipo de comida.
    /// </summary>
    public string? Descricao { get; set; }
}
